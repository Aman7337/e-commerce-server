const mongoose = require("mongoose");

const carousalSchema = new mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
    },
    text: {
      type: String,
      required: true,
    },
    subText: {
      type: String,
      required: true,
    },
    btnText: {
      type: String,
      required: true,
    },
    image: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

module.exports = mongoose.model("Carousal", carousalSchema);
